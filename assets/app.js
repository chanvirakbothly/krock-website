function getLang(){
  return localStorage.getItem("lang") || "km";
}
function setLang(lang){
  localStorage.setItem("lang", lang);
  applyLang();
}
function t(key){
  const lang = getLang();
  return (I18N[lang] && I18N[lang][key]) || (I18N.en[key] ?? key);
}
function applyLang(){
  document.documentElement.lang = getLang() === "km" ? "km" : "en";
  document.querySelectorAll("[data-i18n]").forEach(el=>{
    el.textContent = t(el.getAttribute("data-i18n"));
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el=>{
    el.setAttribute("placeholder", t(el.getAttribute("data-i18n-placeholder")));
  });
  // Update active nav
  const path = location.pathname.split("/").pop() || "index.html";
  document.querySelectorAll("nav a").forEach(a=>{
    const href = a.getAttribute("href");
    a.classList.toggle("active", href === path);
  });
  // Toggle button labels
  const kmBtn = document.getElementById("lang-km");
  const enBtn = document.getElementById("lang-en");
  if(kmBtn && enBtn){
    kmBtn.classList.toggle("primary", getLang()==="km");
    enBtn.classList.toggle("primary", getLang()==="en");
  }
}

document.addEventListener("DOMContentLoaded", ()=>{
  const kmBtn = document.getElementById("lang-km");
  const enBtn = document.getElementById("lang-en");
  if(kmBtn) kmBtn.addEventListener("click", ()=>setLang("km"));
  if(enBtn) enBtn.addEventListener("click", ()=>setLang("en"));
  applyLang();
});
